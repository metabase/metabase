(ns metabase.metabot.agent.profiles
  "Profile configurations for different metabot use cases.
  Each profile defines model settings, iteration limits, and available tools.

  Tools are referenced as zero-arg functions (thunks) that return tool definition maps.
  This enables `defenterprise` tools that return nil in OSS and a full definition in EE."
  (:require
   [clojure.string :as str]
   [malli.error :as me]
   [metabase.llm.settings :as llm]
   [metabase.metabot.agent.tools :as agent-tools]
   [metabase.util.malli :as mu]
   [metabase.util.malli.registry :as mr]))

(def ^:private tool-definition-schema
  [:map
   [:tool-name [:and :string [:fn #(not (str/blank? %))]]]
   [:schema [:fn some?]]
   [:doc {:optional true} [:maybe :string]]
   [:fn [:fn fn?]]
   [:prompt {:optional true} [:maybe :string]]
   [:capabilities {:optional true} [:maybe [:set :keyword]]]
   [:decode {:optional true} [:maybe [:fn fn?]]]
   [:system-instructions {:optional true} [:maybe :string]]])

(def ^:private *profiles
  "Map of profile-id to profile configuration"
  (atom {}))

(defn- validate-tool-definition!
  "Validate a tool definition map (returned from a tool thunk)."
  [tool-def tool-thunk]
  (when-not (mr/validate tool-definition-schema tool-def)
    (throw (ex-info "Invalid tool definition"
                    {:tool-thunk  tool-thunk
                     :definition  tool-def
                     :errors      (me/humanize (mu/explain tool-definition-schema tool-def))})))
  true)

(defn- resolve-tool-definitions
  "Call each tool thunk and return a vector of tool definition maps.
  Filters out nil results (e.g. from defenterprise tools not available in OSS)."
  [tool-thunks]
  (into [] (keep #(%)) tool-thunks))

(mu/defn ^:private register-profile!
  "Register new profile configuration.

  Each profile includes:
  - :name - Keyword identifier for the profile (e.g. :internal)
  - :prompt-template - Selmer template name from resources/metabot/prompts/system/
  - :max-iterations - Maximum agent loop iterations
  - :temperature - LLM temperature setting
  - :tools - Vector of zero-arg functions (thunks) that return tool definition maps

  Tool thunks are called at registration time to validate definitions.
  They are called again at resolution time ([[get-tools-for-profile]]) so that
  defenterprise tools can reflect the current runtime state.

  Note: `:model` is resolved at runtime from the `llm-metabot-provider` setting
  (see [[get-profile]]), not stored in the profile."
  [profile :- [:map
               [:name :keyword]
               [:prompt-template :string]
               [:max-iterations :int]
               [:temperature :float]
               [:tools [:vector :any]]]]
  ;; Validate by calling thunks and checking results (nils from defenterprise are skipped)
  (let [defs (for [thunk (:tools profile)
                   :let  [tool-def (thunk)]
                   :when (and tool-def (validate-tool-definition! tool-def thunk))]
               tool-def)]
    (when-not (apply distinct? (map :tool-name defs))
      (let [dups (->> (frequencies (map :tool-name defs))
                      (filter (fn [[_ cnt]] (< 1 cnt))))]
        (throw (ex-info "Duplicate tool names in profile" {:tool-names (map first dups)})))))
  (swap! *profiles assoc (:name profile) profile))

