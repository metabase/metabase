(deftest all-tools-test
  (testing "profile tools are thunks that return tool definition maps"
    (let [tool-thunks (mapcat :tools (vals @@#'profiles/*profiles))]
      (is (seq tool-thunks))
      (doseq [tool-thunk tool-thunks]
        (is (fn? tool-thunk))
        (let [tool-def (tool-thunk)]
          (is (map? tool-def))
          (is (string? (:tool-name tool-def)))
          (is (some? (:schema tool-def)))
          (is (fn? (:fn tool-def))))))))

(deftest filter-by-capabilities-test
  (testing "returns tools with no capability requirements when capabilities empty"
    (let [tool-defs [(agent-tools/search-tool) (agent-tools/read-resource-tool)]]
      (is (= tool-defs
             (#'profiles/filter-by-capabilities tool-defs #{})))))

  (testing "filters out tools that require missing capabilities"
    (let [tool-defs [(agent-tools/search-tool)
                     (agent-tools/navigate-user-tool)
                     (agent-tools/create-autogenerated-dashboard-tool)]
          capabilities #{}
          result (#'profiles/filter-by-capabilities tool-defs capabilities)]
      (is (= ["search"] (mapv :tool-name result)))))

  (testing "includes tools when capabilities are provided"
    (let [tool-defs [(agent-tools/search-tool) (agent-tools/navigate-user-tool) (agent-tools/create-chart-tool)]
          capabilities #{:frontend-navigate-user-v1}
          result (#'profiles/filter-by-capabilities tool-defs capabilities)]
      (is (= tool-defs result))))

  (testing "filters snippet tools by capabilities"
    (let [tool-defs [(agent-tools/list-snippets-tool) (agent-tools/get-snippet-details-tool) (agent-tools/search-tool)]
          capabilities #{}
          result (#'profiles/filter-by-capabilities tool-defs capabilities)]
      (is (= ["search"] (mapv :tool-name result)))))

  (testing "includes snippet tools when capability present"
    (let [tool-defs [(agent-tools/list-snippets-tool) (agent-tools/get-snippet-details-tool) (agent-tools/search-tool)]
          capabilities #{:feature-snippets}
          result (#'profiles/filter-by-capabilities tool-defs capabilities)]
      (is (= tool-defs result)))))

