"""Query comparison metrics for validating query results."""

import logging
from enum import Enum
from typing import TYPE_CHECKING, Literal

import jinja2
from pydantic import Field

from src.llm import LLM, AvailableModels
from src.metrics.base import ActionResult, MetricCategories, MetricFailure, MetricFailureType
from src.metrics.query_base import BaseQueryEvaluationMetric, MetabotQuery
