(ns metabase.mcp.v2.projections
  "Per-type concise/detailed response projections for v2 MCP read tools, plus the generated
   per-type `fields` dot-path catalog.

   A projection is a subset of the corresponding REST response — same property names, no
   renames. Each tool task registers its own type here; this namespace seeds a few
   representative types so the conventions layer is exercisable.

   The catalog is generated by walking the shape of a sample detailed response (rather than
   hand-listed), so it cannot drift from the projection: dot-paths up to three segments deep,
   item-relative inside arrays."
  (:require
   [clojure.string :as str]
   [medley.core :as m]
   [metabase.dashboards.models.dashboard :as dashboard]
   [metabase.models.interface :as mi]
   [metabase.util :as u]))

(set! *warn-on-reflection* true)

(defonce ^:private registry
  (atom {}))

;;; ------------------------------------------------ Catalog generation --------------------------------------------

(def ^:private max-catalog-depth 3)

(defn- sample-paths
  [prefix node]
  (cond
    (= (count prefix) max-catalog-depth)
    [(str/join "." prefix)]

    (map? node)
    (if (empty? node)
      (when (seq prefix) [(str/join "." prefix)])
      (mapcat (fn [[k v]] (sample-paths (conj prefix (name k)) v)) node))

    ;; Arrays are item-relative: the path addresses fields of the items, the array itself
    ;; contributes no segment.
    (sequential? node)
    (if-let [item (first (filter map? node))]
      (sample-paths prefix item)
      (when (seq prefix) [(str/join "." prefix)]))

    :else
    (when (seq prefix) [(str/join "." prefix)])))

(defn paths-from-sample
  "Return the vector of `fields` dot-paths derivable from `sample` (a built detailed response
   map): every leaf up to three segments deep, item-relative inside arrays."
  [sample]
  (vec (sort (distinct (sample-paths [] sample)))))

;;; ------------------------------------------------ Registry -------------------------------------------------------

(defn register-projection!
  "Register the projection entry for `type` (a keyword such as `:collection`). Overwrites any
   existing entry, so re-evaluating a tool namespace in the REPL is idempotent.

   `entry` keys:
   - `:concise` / `:detailed` — `(fn [row] response-map)`, REST property names only
   - `:sample` — a representative detailed row; the catalog is generated from
     `(:detailed entry)` applied to it
   - `:catalog` — explicit dot-path vector, only for types whose sample can't capture the
     shape (prefer `:sample`)"
  [type {:keys [concise detailed sample] :as entry
         explicit-catalog :catalog}]
  {:pre [(keyword? type) (fn? concise) (fn? detailed) (or sample explicit-catalog)]}
  (let [resolved-catalog (or explicit-catalog (paths-from-sample (detailed sample)))]
    (swap! registry assoc type (assoc entry :catalog resolved-catalog))
    type))

(defn register-key-projection!
  "Register `type`'s concise/detailed projections as `select-keys` over the given key vectors —
   the common case, replacing a hand-written `{:concise … :detailed … :sample …}` map. Options:

   - `:detailed-keys` — keys for the detailed projection; defaults to `concise-keys`.
   - `:compact?` — drop nil-valued keys from the projected row (default true). Pass false for rows
     whose columns are always populated and where an explicit null is meaningful.
   - `:sample` — a representative detailed row, overriding the auto `(zipmap detailed-keys \"x\")`
     for types with nested (non-scalar) shapes the catalog must see."
  [type concise-keys & {:keys [detailed-keys compact? sample] :or {compact? true}}]
  (let [detailed-keys (or detailed-keys concise-keys)
        project-fn    (fn [ks]
                        (if compact?
                          #(m/remove-vals nil? (select-keys % ks))
                          #(select-keys % ks)))]
    (register-projection!
     type
     {:concise  (project-fn concise-keys)
      :detailed (project-fn detailed-keys)
      :sample   (or sample (zipmap detailed-keys (repeat "x")))})))

(defn project
  "Apply `type`'s `fmt` (`:concise` | `:detailed`) projection to `row`. Throws when no
   projection is registered for `type`."
  [type fmt row]
  (let [entry (get @registry type)]
    (when-not entry
      (throw (ex-info (str "No projection registered for type: " (name type))
                      {:status-code 500 :type type})))
    ((get entry fmt) row)))

(defn catalog
  "The `fields` dot-path catalog for `type`, or nil when the type is unregistered."
  [type]
  (get-in @registry [type :catalog]))

(defn all-catalogs
  "Map of registered type -> dot-path catalog, for publishing as an MCP resource."
  []
  (into (sorted-map) (map (fn [[type entry]] [type (:catalog entry)])) @registry))

;;; --------------------------------------------- Representative types ---------------------------------------------
;; Field selection ported from the v1 presenters (metabase.metabot.tools.resources), but keeping
;; the REST property names — v2 never renames (`db_id` stays `db_id`, no synthetic `uri`/`path`).

(def ^:private collection-concise-keys
  [:id :name :description :location :archived])

(def ^:private collection-detailed-keys
  (into collection-concise-keys
        [:authority_level :personal_owner_id :namespace :entity_id :slug :created_at]))

(register-key-projection! :collection collection-concise-keys
                          :detailed-keys collection-detailed-keys :compact? false)

(def ^:private table-concise-keys
  [:id :name :display_name :schema :db_id :description])

(def ^:private table-detailed-keys
  (into table-concise-keys
        [:entity_type :view_count :active :field_order :is_upload :visibility_type
         :initial_sync_status :created_at :updated_at]))

(register-key-projection! :table table-concise-keys
                          :detailed-keys table-detailed-keys :compact? false)

;; The one canonical card projection. Shared by browse_collection's model list and get_content's
;; question/model reads — registered here (the namespace both load) so it has a single definition
;; rather than one tool silently overwriting another's at load time. `query_summary`/`template_tags`
;; are enrichments get_content computes; browse rows lack them and `compact` drops them.
(def ^:private question-concise-keys
  [:id :name :type :description :display :collection_id :database_id :table_id :source_card_id
   :archived :query_summary :template_tags :parameters])

(def question-detailed-keys
  "Keys of the `:question` detailed projection. All are Card columns except
   [[question-enrichment-keys]], which `get_content` computes at read time."
  (into question-concise-keys
        [:entity_id :dashboard_id :query_type :collection_position :creator_id :cache_ttl
         :created_at :updated_at]))

(def question-enrichment-keys
  "Projection keys `get_content` computes at read time — not Card columns. Column-select paths
   (`list_models`, browse rows) can't fetch them, so they drop these before selecting."
  #{:query_summary :template_tags})

(register-key-projection!
 :question question-concise-keys
 :detailed-keys question-detailed-keys
 :sample (-> (zipmap question-detailed-keys (repeat "x"))
             (assoc :template_tags {}
                    :parameters [{:id "x" :name "x" :type "x" :target ["x"] :slug "x"}])))

;;; ----------------------------------------------- dashboard ------------------------------------------------------

(defn- dashcard-kind
  [{:keys [card_id action_id visualization_settings]}]
  (cond
    action_id "action"
    card_id   "card"
    :else     (or (some->> (get-in visualization_settings [:virtual_card :display]) name (str "virtual_"))
                  "virtual")))

(defn- dashcard-card-ref
  "The `{id, name}` card reference for a card-backed dashcard; unreadable cards collapse to
   `{id}` only, as the REST dashboard response does."
  [{:keys [card_id action_id card]}]
  (when (and card_id (not action_id))
    (if (and card (mi/can-read? card))
      {:id (:id card) :name (:name card)}
      {:id card_id})))

(defn- dashcard-summary
  [{:keys [visualization_settings] :as dc}]
  (m/remove-vals
   nil?
   {:id                (:id dc)
    :kind              (dashcard-kind dc)
    :card              (dashcard-card-ref dc)
    ;; External link URLs only — a link card's stored entity snapshot may describe something
    ;; this user can't read, so it never rides the summary.
    :text              (when-not (:card_id dc)
                         (or (:text visualization_settings)
                             (get-in visualization_settings [:link :url])))
    :dashboard_tab_id  (:dashboard_tab_id dc)
    :row               (:row dc)
    :col               (:col dc)
    :size_x            (:size_x dc)
    :size_y            (:size_y dc)
    :series            (not-empty
                        (mapv (fn [s] (if (mi/can-read? s) {:id (:id s) :name (:name s)} {:id (:id s)}))
                              (:series dc)))
    :inline_parameters (not-empty (:inline_parameters dc))}))

(defn- dashboard-parameters-summary
  "One row per dashboard parameter: id, name, type, and the ids of the dashcards it is wired
   to (via [[dashboard/dashboard->resolved-params]], the same resolution the QP uses)."
  [dash]
  (let [resolved (dashboard/dashboard->resolved-params dash)]
    (mapv (fn [{param-id :id :as param}]
            (m/remove-vals
             nil?
             {:id           param-id
              :name         (:name param)
              :type         (:type param)
              :dashcard_ids (not-empty
                             (vec (sort (distinct (keep (comp :id :dashcard)
                                                        (:mappings (get resolved (u/qualified-name param-id))))))))}))
          (:parameters dash))))

(defn dashboard-row
  "The projection row for `dash`, a dashboard hydrated with `[:dashcards :series :card] :tabs`.
  Nested cards the current user cannot read collapse to `{:id …}`, so this must be called as the
  requesting user. Both the `:concise` and `:detailed` projections select from this shape."
  [dash]
  (-> (select-keys dash [:id :name :description :entity_id :collection_id :creator_id :archived
                         :created_at :updated_at :width :auto_apply_filters :cache_ttl
                         :collection_position])
      (assoc :tabs       (mapv #(select-keys % [:id :name]) (:tabs dash))
             :parameters (dashboard-parameters-summary dash)
             :dashcards  (mapv dashcard-summary
                               (sort-by (juxt #(or (:row %) 0) #(or (:col %) 0)) (:dashcards dash))))))

(def ^:private dashboard-concise-keys
  [:id :name :description :tabs :parameters :dashcards])

(def ^:private dashboard-detailed-keys
  (into dashboard-concise-keys
        [:entity_id :collection_id :creator_id :archived :created_at :updated_at :width
         :auto_apply_filters :cache_ttl :collection_position]))

(def ^:private dashboard-sample
  (-> (zipmap dashboard-detailed-keys (repeat "x"))
      (assoc :tabs [{:id 1 :name "x"}]
             :parameters [{:id "x" :name "x" :type "x" :dashcard_ids [1]}]
             :dashcards [{:id 1 :kind "x" :card {:id 1 :name "x"} :text "x" :dashboard_tab_id 1
                          :row 0 :col 0 :size_x 1 :size_y 1 :series [{:id 1 :name "x"}]
                          :inline_parameters ["x"]}])))

(register-key-projection!
 :dashboard dashboard-concise-keys
 :detailed-keys dashboard-detailed-keys
 :sample dashboard-sample)
