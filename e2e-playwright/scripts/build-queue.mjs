#!/usr/bin/env node
/**
 * Builds the continuous-dispatch queue: every unported Cypress scenario spec,
 * classified by size and gating tags, sorted for dispatch (big specs first so
 * they never become tail latency; container-gated specs flagged).
 *
 * Usage: node scripts/build-queue.mjs   (from e2e-playwright/)
 * Reads  PORTED.txt (one source-spec path per line, # comments ok)
 * Writes QUEUE.md (human) — the orchestrator dispatches from the top.
 */
import { execSync } from "child_process";
import fs from "fs";
import path from "path";

const ROOT = path.resolve(import.meta.dirname, "../..");
const SCENARIOS = path.join(ROOT, "e2e/test/scenarios");

const ported = new Set(
  fs
    .readFileSync(path.resolve(import.meta.dirname, "../PORTED.txt"), "utf8")
    .split("\n")
    .map((line) => line.trim())
    .filter((line) => line && !line.startsWith("#")),
);

const files = execSync(
  `find ${SCENARIOS} -name "*.cy.spec.*" -type f | sort`,
  { encoding: "utf8" },
)
  .trim()
  .split("\n");

const rows = [];
for (const file of files) {
  const rel = path.relative(SCENARIOS, file);
  if (ported.has(rel)) continue;
  const content = fs.readFileSync(file, "utf8");
  const lines = content.split("\n").length;
  const tags = [];
  if (/@external/.test(content)) tags.push("external");
  if (/@mongo/.test(content)) tags.push("mongo");
  if (/@OSS/.test(content)) tags.push("oss");
  if (/@skip/.test(content)) tags.push("has-skips");
  if (/snowplow/i.test(content)) tags.push("snowplow");
  if (/setupSMTP|getInbox|maildev/i.test(content)) tags.push("email");
  if (/webhook/i.test(content)) tags.push("webhook");
  if (/activateToken/.test(content)) tags.push("token");
  rows.push({ rel, lines, tags });
}

rows.sort((a, b) => b.lines - a.lines);

const out = [
  "# Dispatch queue (generated by scripts/build-queue.mjs)",
  "",
  `${rows.length} specs remaining, ${rows.reduce((s, r) => s + r.lines, 0)} lines total.`,
  "Dispatch from the top (largest first — they must never be tail latency).",
  "",
  "| lines | spec | gates |",
  "|---|---|---|",
  ...rows.map((r) => `| ${r.lines} | ${r.rel} | ${r.tags.join(", ")} |`),
  "",
];
fs.writeFileSync(path.resolve(import.meta.dirname, "../QUEUE.md"), out.join("\n"));
console.log(`${rows.length} specs queued (${rows.reduce((s, r) => s + r.lines, 0)} lines). Top 5:`);
for (const r of rows.slice(0, 5)) console.log(`  ${r.lines}  ${r.rel}  [${r.tags}]`);
