"""Semantic validation metrics using LLM as judge."""

from typing import TYPE_CHECKING

import jinja2
from pydantic import Field

from src.llm import LLM, AvailableModels
from src.metrics.base import ActionResult, MetricCategories
from src.metrics.query_base import BaseQueryEvaluationMetric, MetabotQuery
from src.types import NoAdditionalPropsBaseModel, UserMessage

if TYPE_CHECKING:
    from src.core.test_case import E2EAgentTestCase


class OutputCheckResult(NoAdditionalPropsBaseModel):
    comment: str = Field(description="Concise comment on why you chose true or false.")
    matches_expectation: bool = Field(description="Whether the response matches the expectation.")


class QueryMatchesDescription(BaseQueryEvaluationMetric):
    """Validate that agent-generated queries match expected business logic.

    Uses an LLM as a judge to evaluate whether the agent's query satisfies
    the specified requirements.

    See also:
        - QueryMatchesReference: For exact comparison against a reference query
        - QueryUsesModel/QueryUsesMetrics: For simple data source validation
    """

    query_description: str = Field(description="Description of the expected query to be created.")
    convert_mbql_to_sql: bool = Field(
        default=True, description="Whether to convert MBQL queries to SQL for comparison."
    )
    category = MetricCategories.QUERY_LOGICAL_CORRECTNESS

    async def check_output_matches_expectation(self, query: MetabotQuery) -> OutputCheckResult:
        llm = LLM()

        message_content = """
Your task is to determine whether a SQL or MBQL (Metabase query language) query
generated by an AI agent matches a description of the expected query.
Evaluate based on semantic equivalence and business intent, not just syntactic similarity.

EXPECTED QUERY DESCRIPTION:
<expected_query_description>
{{ query_description }}
</expected_query_description>

AGENT-GENERATED QUERY:
The `type` attribute indicates whether the original query is in MBQL or SQL format.
<query type={{ query.type }}>
{% if convert_mbql_to_sql or query.type == "SQL" %}
{{ query.sql_representation }}
{% else %}
{{ query.raw_query.to_mbql() }}
{% endif %}
</query>

EVALUATION CRITERIA:

Core Requirements (must match for a positive evaluation):
1. DATA SOURCES: Query uses appropriate tables/models mentioned in the description.
2. JOIN LOGIC: If joins are required, they should be logically sound.
3. FILTERING: Query applies filters that align with the description's intent.
4. AGGREGATIONS: If aggregations are mentioned, the query should perform them on appropriate fields.
5. GROUPING: Query groups data appropriately for the described analysis.
6. OUTPUT COLUMNS: Query includes the key data points mentioned in the description.

ACCEPTABLE VARIATIONS:
- Different but equivalent SQL constructs (CTEs vs subqueries, implicit vs explicit joins)
- Different aliases for tables and columns
- Extra columns beyond what's strictly described (additional context is OK - except if explicitly forbidden)
- Different column ordering in SELECT clause
- Different but semantically equivalent expressions or calculations
- Equivalent boundary conditions in filters (e.g., "x > 100" vs "x >= 101" for integers)
- Semantically equivalent date/time filtering
- Different ORDER BY that achieves similar business goals

NOT ACCEPTABLE:
- Missing core data sources mentioned in the description
- Wrong aggregation functions (e.g., SUM instead of AVG when AVG is clearly needed)
- Contradictory filtering logic
- Missing required columns that are central to the description
- Fundamentally different business logic

HANDLING OPTIONAL REQUIREMENTS:
- Elements marked "optional", "not required", "may", or "can be" are NOT mandatory
- "Or" between alternatives means ANY ONE is sufficient

Respond with:
- matches_expectation: true if the query satisfies the description's intent, false otherwise
- comment: Concise explanation (2-3 sentences) of your evaluation
"""
        message_content = jinja2.Template(message_content).render(
            query_description=self.query_description, query=query, convert_mbql_to_sql=self.convert_mbql_to_sql
        )

        completion = await llm.structured_completion(
            model=AvailableModels.ANTHROPIC_CLAUDE_HAIKU_4_5,
            messages=[UserMessage(content=message_content)],
            response_format=OutputCheckResult,
        )

        res = completion.response
        return res

    async def measure(self, test_case: "E2EAgentTestCase"):
        self.action_result = ActionResult(
            is_action_needed=True,
            is_action_hallucination=False,
            is_hesitation=False,
        )
        query = await self.get_query(test_case)
        if not query:
            self.score = 0
            self.action_result.is_hesitation = True
            self.reason = "No queries found in the response."
            return

        res = await self.check_output_matches_expectation(query=query)
        if not res.matches_expectation:
            self.action_result.is_action_hallucination = True
        self.score = int(res.matches_expectation)
        self.reason = res.comment
