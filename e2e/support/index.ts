const shouldWrapHelper = (key: string): boolean =>
  WRAPPED_HELPERS.includes(key);

const wrappedHelpers = Object.entries(OriginalHelpers).reduce<
  Record<string, any>
>((acc, [key, helper]) => {
  acc[key] = shouldWrapHelper(key)
    ? (...args: any[]) => {
        return cy.then(() => {
          Cypress.log({
            name: `H.${key}`,
            message: formatArgs(args),
            consoleProps: () => ({
              Function: key,
              Arguments: args,
            }),
          });

          return helper(...args);
        });
      }
    : helper;
  return acc;
}, {});

// Export with proper type annotation
export const H = wrappedHelpers;
