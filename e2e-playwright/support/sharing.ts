/**
 * Port of H.NativeEditor.type. Only the escape sequences this spec needs are
 * implemented; everything else is typed as literal keystrokes (which, like
 * Cypress realType, lets CodeMirror auto-close `{{` into `{{}}`).
 */
export async function typeInNativeEditor(page: Page, text: string) {
  await focusNativeEditor(page);
  const parts = text.split(/(\{[^{}]+\})/).filter((part) => part !== "");
  for (const part of parts) {
    switch (part.toLowerCase()) {
      case "{home}":
      case "{movetostart}":
        await page.keyboard.press("Home");
        break;
      case "{end}":
      case "{movetoend}":
        await page.keyboard.press("End");
        break;
      default:
        await page.keyboard.type(part, { delay: 20 });
    }
  }
}

// === port of H.saveQuestion (e2e-misc-helpers.js), collection-save subset ===

export async function saveQuestion(
  page: Page,
  name: string,
  { path }: { path?: (string | RegExp)[] } = {},
): Promise<number> {
  const saveResponse = page.waitForResponse(
    (response) =>
      response.request().method() === "POST" &&
      new URL(response.url()).pathname === "/api/card",
  );

  await page
    .getByTestId("qb-header")
    .getByRole("button", { name: "Save", exact: true })
    .click();

  const saveModal = page.getByTestId("save-question-modal");
  await saveModal.getByLabel("Name", { exact: true }).fill(name);
  if (path) {
    await saveModal.getByLabel(/Where do you want to save this/).click();
    await pickEntity(page, { path, select: true });
  }
  await saveModal.getByRole("button", { name: "Save", exact: true }).click();

  const body = (await (await saveResponse).json()) as {
    id: number;
    dashboard_id: number | null;
  };

  // Port of checkSavedToCollectionQuestionToast.
  if (!body.dashboard_id) {
    await expect(
      page.getByTestId("toast-undo").getByText(/Saved/i),
    ).toBeVisible();
  }

  return body.id;
}

// === port of H.downloadAndAssert (e2e-downloads-helpers.ts), UI subset ===

/**
 * Drives the question download UI and resolves with the resulting Download.
 * Unlike the Cypress helper (which intercepts the request and never lets the
 * download happen), this waits for a real browser download — assert on
 * download.url() / suggestedFilename() at the call site.
 */
export async function downloadViaUi(
  page: Page,
  {
    fileType,
    enableFormatting = true,
  }: { fileType: "csv" | "xlsx"; enableFormatting?: boolean },
): Promise<Download> {
  await page.getByLabel("Download results", { exact: true }).click();

  const menu = popover(page);
  await menu.getByText(`.${fileType}`, { exact: true }).click();

  const formattingCheckbox = menu.getByTestId("keep-data-formatted");
  if ((await formattingCheckbox.isChecked()) !== enableFormatting) {
    await formattingCheckbox.click();
  }

  const download = page.waitForEvent("download");
  await menu.getByTestId("download-results-button").click();
  return download;
}
