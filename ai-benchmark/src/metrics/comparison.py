if TYPE_CHECKING:
    from src.core.test_case import E2EAgentTestCase

logger = logging.getLogger(__name__)


class QueryMatchLevel(str, Enum):
    """Enum representing how well a query matches expectations."""

    EXACT_MATCH = "exact_match"
    SEMANTIC_MATCH = "semantic_match"
    ALTERNATE_INTERPRETATION = "alternate_interpretation"
    MISMATCH = "mismatch"


class QueryMatchResult(NoAdditionalPropsBaseModel):
    """Result of comparing a query against expectations."""

    comment: str = Field(description="Concise explanation of the match level.")
    match_level: QueryMatchLevel

    @property
    def matches_expectation(self) -> bool:
        """For backward compatibility."""
        return self.match_level in (QueryMatchLevel.EXACT_MATCH, QueryMatchLevel.SEMANTIC_MATCH)


class ResultComparisonOutput(NoAdditionalPropsBaseModel):
    """Structured output for LLM-based query result comparison."""

    results_match: bool = Field(description="Whether the two result sets are semantically equivalent.")
    confidence: Literal["high", "medium", "low"] = Field(description="Confidence level in the comparison judgment.")
    comment: str = Field(description="Brief explanation of your judgment.")


class QueryMatchesReference(BaseQueryEvaluationMetric):
    """Validate that the agent's query matches a reference SQL query.

    Uses an LLM as a judge to compare the agent's query against a known-good reference.
    """

    reference_query: str = Field(description="The reference SQL query to compare against.")
    category = MetricCategories.QUERY_LOGICAL_CORRECTNESS

    async def check_output_matches_expectation(self, query: MetabotQuery, original_question: str) -> QueryMatchResult:
        llm = LLM()

        message_content = """
Your task is to determine how well a SQL query generated by an AI agent matches a reference SQL query.
Evaluate the match level based on semantic equivalence and business intent.

ORIGINAL USER QUESTION:
<user_question>
{{ original_question }}
</user_question>

REFERENCE SQL QUERY:
<reference_query>
{{ reference_query }}
</reference_query>

AGENT-GENERATED SQL QUERY:
<agent_query>
{{ agent_query }}
</agent_query>

MATCH LEVELS:

EXACT_MATCH:
- Queries are syntactically identical or only differ in trivial ways

SEMANTIC_MATCH:
- Queries will produce the SAME RESULTS on the same dataset
- All core requirements are met

ALTERNATE_INTERPRETATION:
- Query is valid and addresses the original question
- Uses different approach or business logic than reference

MISMATCH:
- Query does not address the original question correctly
- Uses different tables or data sources inappropriately
- Missing required columns from reference

Respond with:
- comment: Concise explanation (2-3 sentences) of your decision
- match_level: One of [exact_match, semantic_match, alternate_interpretation, mismatch]
"""
        message_content = jinja2.Template(message_content).render(
            reference_query=self.reference_query,
            agent_query=query.sql_representation,
            original_question=original_question,
        )
        completion = await llm.structured_completion(
            model=AvailableModels.ANTHROPIC_CLAUDE_HAIKU_4_5,
            messages=[UserMessage(content=message_content)],
            response_format=QueryMatchResult,
        )
        res = completion.response
        return res

    async def measure(self, test_case: "E2EAgentTestCase"):
        self.action_result = ActionResult(
            is_action_needed=True,
            is_action_hallucination=False,
            is_hesitation=False,
        )

        query = await self.get_query(test_case)
        if not query:
            self.action_result.is_hesitation = True
            self.reason = "No queries found in the response."
            return

        res = await self.check_output_matches_expectation(query=query, original_question=test_case.message)
        if not res.matches_expectation:
            self.action_result.is_action_hallucination = True
        self.score += int(res.matches_expectation)
        self.reason = res.comment


class QueryResultsMatchLLM(BaseQueryEvaluationMetric):
    """Use an LLM judge to validate semantic equivalence of query results.

    Executes both the agent's query and a reference query, then uses an LLM
    to judge whether the results are semantically equivalent.
    """

    category = MetricCategories.QUERY_LOGICAL_CORRECTNESS

    reference_query: str = Field(description="The reference SQL query that produces the expected results.")
    database_id: int = Field(description="Database ID to execute both queries against.")
    max_rows_to_compare: int = Field(default=100, description="Maximum number of rows to show the LLM for comparison.")
    llm_model: AvailableModels = Field(
        default=AvailableModels.ANTHROPIC_CLAUDE_HAIKU_4_5,
        description="LLM model to use for comparison judgment.",
    )

    async def _execute_sql_query(self, client, sql: str) -> dict:
        """Execute a raw SQL query and return results."""
        query_dict = {
            "database": self.database_id,
            "type": "native",
            "native": {"query": sql},
        }
        return await client.run_query(query_dict)

    def _format_result_for_llm(self, result: dict, max_rows: int) -> str:
        """Format query result for LLM comparison."""
        rows = result.get("data", {}).get("rows", [])
        columns = result.get("data", {}).get("columns", [])

        if not rows:
            return "Empty result set (0 rows)"

        total_rows = len(rows)
        rows_to_show = rows[:max_rows]

        lines = [f"Columns: {columns}", f"Row count: {total_rows}", ""]

        for i, row in enumerate(rows_to_show):
            lines.append(f"{i}: {row}")

        if total_rows > max_rows:
            lines.append(f"... ({total_rows - max_rows} more rows not shown)")

        return "\n".join(lines)

    async def _llm_compare(self, agent_query: str, agent_result: dict, ref_result: dict) -> ResultComparisonOutput:
        """Use LLM to compare query results for semantic equivalence."""

        prompt = f"""Compare these two SQL query results for semantic equivalence.

REFERENCE QUERY:
{self.reference_query}

REFERENCE QUERY RESULTS:
{self._format_result_for_llm(ref_result, self.max_rows_to_compare)}

AGENT QUERY:
{agent_query}

AGENT QUERY RESULTS:
{self._format_result_for_llm(agent_result, self.max_rows_to_compare)}

CRITICAL: The agent is ALLOWED to include extra columns beyond what the reference query has.
Focus on whether the agent results contain AT LEAST the same essential data as the reference.

Respond with your judgment:
- results_match: true if agent results contain the same essential data (extra columns are OK)
- confidence: "high", "medium", or "low"
- comment: brief explanation of your judgment
"""

        llm = LLM()
        completion = await llm.structured_completion(
            model=self.llm_model,
            messages=[UserMessage(content=prompt)],
            response_format=ResultComparisonOutput,
        )

        return completion.response

    async def measure(self, test_case: "E2EAgentTestCase"):
        self.action_result = ActionResult(
            is_action_needed=True,
            is_action_hallucination=False,
            is_hesitation=False,
        )

        try:
            query = await self.get_query(test_case)
            if not query:
                self.action_result.is_hesitation = True
                self.reason = "No queries found in the response."
                return

            if not query.sql_representation:
                self.action_result.is_action_hallucination = True
                self.reason = "Failed to get SQL representation of the agent query."
                return

            client = test_case.get_admin_client()

            try:
                agent_result = await self._execute_sql_query(client, query.sql_representation)
            except Exception as e:
                self.action_result.is_action_hallucination = True
                self.reason = f"Agent query failed to execute: {str(e)}"
                return

            if "error" in agent_result:
                self.action_result.is_action_hallucination = True
                self.reason = f"Agent query returned error: {agent_result['error']}"
                return

            try:
                reference_result = await self._execute_sql_query(client, self.reference_query)
            except Exception as e:
                self.score = 0
                self.reason = f"TEST ERROR: Reference query failed to execute: {str(e)}"
                return

            if "error" in reference_result:
                self.score = 0
                self.reason = f"TEST ERROR: Reference query returned error: {reference_result['error']}"
                return

            comparison = await self._llm_compare(query.sql_representation, agent_result, reference_result)

            if not comparison.results_match:
                self.action_result.is_action_hallucination = True

            self.score = int(comparison.results_match)

            confidence_note = f" (confidence: {comparison.confidence})"
            self.reason = f"{comparison.comment}{confidence_note}"

        except Exception as e:
            self.score = 0
            self.reason = f"Unexpected error during measurement: {str(e)}"
            logger.exception("Error in QueryResultsMatchLLM.measure")


class QueryResultsMatch(BaseQueryEvaluationMetric):
    """Deterministic query result comparison using modular evaluators.

    Replaces LLM-based result comparison with deterministic, hash-based evaluation.
    """

    category = MetricCategories.QUERY_LOGICAL_CORRECTNESS

    reference_query: str = Field(description="The reference SQL query that produces the expected results.")
    database_id: int = Field(description="Database ID to execute both queries against.")
    evaluators: list = Field(description="List of evaluators to run. All must pass by default.")
    require_all: bool = Field(
        default=True,
        description="Whether all evaluators must pass (AND logic) or any one is sufficient (OR logic).",
    )

    async def _execute_sql_query(self, client, sql: str) -> dict:
        """Execute a raw SQL query and return results."""
        query_dict = {
            "database": self.database_id,
            "type": "native",
            "native": {"query": sql},
        }
        return await client.run_query(query_dict)

    def _build_reason(self, results: list) -> str:
        """Build detailed reason from evaluator results."""
        failed = [r for r in results if not r.passed]
        passed = [r for r in results if r.passed]

        if not failed:
            return "All evaluators passed: " + "; ".join(r.message for r in results)

        failure_messages = [r.message for r in failed]
        reason = f"Evaluator failures: {'; '.join(failure_messages)}"

        if passed:
            reason += f" (Passed: {len(passed)}/{len(results)})"

        return reason

    async def measure(self, test_case: "E2EAgentTestCase"):
        self.action_result = ActionResult(
            is_action_needed=True,
            is_action_hallucination=False,
            is_hesitation=False,
        )
        self.failures = []

        try:
            query = await self.get_query(test_case)
            if not query:
                self.action_result.is_hesitation = True
                self.reason = "No queries found in the response."
                self.failures.append(
                    MetricFailure(
                        message="No queries found in the response.",
                        error=None,
                        failure_type=MetricFailureType.NO_QUERIES_FOUND,
                    )
                )
                return

            if not query.sql_representation:
                self.action_result.is_action_hallucination = True
                self.score = 0
                self.reason = "Failed to get SQL representation of the agent query."
                self.failures.append(
                    MetricFailure(
                        message="Failed to get SQL representation of the agent query.",
                        error=None,
                        failure_type=MetricFailureType.SQL_REPRESENTATION_FAILED,
                    )
                )
                return

            client = test_case.get_admin_client()

            try:
                agent_result = await self._execute_sql_query(client, query.sql_representation)
            except Exception as e:
                self.action_result.is_action_hallucination = True
                self.score = 0
                self.reason = f"Agent query failed to execute: {str(e)}"
                self.failures.append(
                    MetricFailure(
                        message=f"Agent query failed to execute: {str(e)}",
                        error=str(e),
                        failure_type=MetricFailureType.AGENT_QUERY_EXECUTION_FAILED,
                    )
                )
                return

            if "error" in agent_result:
                self.action_result.is_action_hallucination = True
                self.score = 0
                self.reason = f"Agent query returned error: {agent_result['error']}"
                self.failures.append(
                    MetricFailure(
                        message=f"Agent query returned error: {agent_result['error']}",
                        error=agent_result["error"],
                        failure_type=MetricFailureType.AGENT_QUERY_ERROR,
                    )
                )
                return

            try:
                reference_result = await self._execute_sql_query(client, self.reference_query)
            except Exception as e:
                self.score = 0
                self.reason = f"TEST ERROR: Reference query failed to execute: {str(e)}"
                self.failures.append(
                    MetricFailure(
                        message=f"TEST ERROR: Reference query failed to execute: {str(e)}",
                        error=str(e),
                        failure_type=MetricFailureType.REFERENCE_QUERY_EXECUTION_FAILED,
                    )
                )
                return

            if "error" in reference_result:
                self.score = 0
                self.reason = f"TEST ERROR: Reference query returned error: {reference_result['error']}"
                self.failures.append(
                    MetricFailure(
                        message=f"TEST ERROR: Reference query returned error: {reference_result['error']}",
                        error=reference_result["error"],
                        failure_type=MetricFailureType.REFERENCE_QUERY_ERROR,
                    )
                )
                return

            agent_rows = agent_result.get("data", {}).get("rows", [])
            ref_rows = reference_result.get("data", {}).get("rows", [])

            results = []
            for evaluator in self.evaluators:
                eval_result = evaluator.evaluate(agent_rows, ref_rows)
                results.append(eval_result)

            if self.require_all:
                passed = all(r.passed for r in results)
            else:
                passed = any(r.passed for r in results)

            self.score = 1 if passed else 0

            if not passed:
                self.action_result.is_action_hallucination = True
                for eval_result in results:
                    if not eval_result.passed and eval_result.failure_type:
                        self.failures.append(
                            MetricFailure(
                                message=eval_result.message,
                                error=eval_result.details.get("error")
                                if isinstance(eval_result.details, dict)
                                else None,
                                failure_type=eval_result.failure_type,
                            )
                        )

            self.reason = self._build_reason(results)

        except Exception as e:
            self.score = 0
            self.reason = f"Unexpected error during measurement: {str(e)}"
            self.failures.append(
                MetricFailure(
                    message=f"Unexpected error during measurement: {str(e)}",
                    error=str(e),
                    failure_type=MetricFailureType.UNEXPECTED_ERROR,
                )
            )
            logger.exception("Error in QueryResultsMatch.measure")
