(ns ^:mb/driver-tests metabase.test.data.bigquery-cloud-sdk-test
  "Tests for the BigQuery test extensions themselves — specifically the CI dataset-naming scheme."
  (:require
   [clojure.string :as str]
   [clojure.test :refer :all]
   [metabase.config.core :as config]
   [metabase.test.data.bigquery-cloud-sdk :as bigquery.tx]
   [metabase.test.data.dataset-definitions :as defs]
   [metabase.test.data.interface :as tx]))

(deftest dataset-naming-version-splice-test
  (testing (str "\ndiagnostics: "
                (pr-str {:github-ref-name              (System/getenv "GITHUB_REF_NAME")
                         :on-master-or-release-branch? (tx/on-master-or-release-branch?)
                         :mb-version-info              config/mb-version-info}))
    (testing "current-major-version must be known wherever dataset names get the release-isolation version splice"
      ;; [[config/current-major-version]] comes from `version.properties`, which is generated by the build and does
      ;; not exist when tests run from source (as they do in CI driver-test jobs) — in that case it is nil.
      (is (some? (config/current-major-version))))
    (testing "release-branch dataset names must not collide with the dev-branch `sha__` pool"
      ;; force the release arm of [[bigquery.tx/test-dataset-id]]: on `pull_request` CI runs GITHUB_REF_NAME is
      ;; `<pr>/merge`, so the branch check alone would route this PR's own CI run down the dev arm and hide the bug.
      (with-redefs [tx/on-master-or-release-branch? (constantly true)]
        (let [dataset-id (bigquery.tx/test-dataset-id (tx/get-dataset-definition defs/test-data))]
          (is (not (str/starts-with? dataset-id "sha__"))
              (str "release-arm naming degenerated to the dev-pool prefix: " dataset-id)))))))
