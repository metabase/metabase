// === port of H.visitPublicQuestion (e2e-misc-helpers.js) ===

export async function visitPublicQuestion(
  page: Page,
  mb: SessionHarness,
  id: number,
  {
    params = {},
    hash = {},
  }: {
    params?: Record<string, string>;
    hash?: Record<string, string>;
  } = {},
) {
  const { uuid } = await createPublicQuestionLink(mb.api, id);
  const searchParams = new URLSearchParams(params).toString();
  const searchSection = searchParams ? `?${searchParams}` : "";
  const hashParams = new URLSearchParams(hash).toString();
  const hashSection = hashParams ? `#${hashParams}` : "";

  await mb.signOut();
  await page.goto(`/public/question/${uuid}${searchSection}${hashSection}`);
}

/**
 * Port of cy.signIn for users outside the fixture's UserName union (e.g.
 * "none"): injects the cached snapshot session as cookies, same mechanism as
 * MetabaseHarness.signIn.
 */
export async function signInWithCachedSession(
  context: BrowserContext,
  user: string,
) {
  const cached = LOGIN_CACHE[user];
  if (!cached) {
    throw new Error(`No cached session for user "${user}"`);
  }
  const { hostname } = new URL(BASE_URL);
  const cookie = { domain: hostname, path: "/" };
  await context.addCookies([
    {
      name: "metabase.SESSION",
      value: cached.sessionId,
      httpOnly: true,
      ...cookie,
    },
    { name: "metabase.TIMEOUT", value: "alive", ...cookie },
    {
      name: "metabase.DEVICE",
      value: cached.deviceId,
      httpOnly: true,
      ...cookie,
    },
  ]);
}

// === ports of e2e-ad-hoc-question-helpers.js ===

function adhocQuestionHash(question: Record<string, unknown>): string {
  const questionWithDisplay = {
    display: "table",
    // without "locking" the display, the QB will run its picking logic and
    // override the setting
    displayIsLocked: question.display != null,
    ...question,
  };
  return Buffer.from(JSON.stringify(questionWithDisplay)).toString("base64");
}

/** Port of H.startNewNativeQuestion — the query generated by "New" > "SQL query". */
export async function startNewNativeQuestion(
  page: Page,
  {
    database = SAMPLE_DB_ID,
    query = "",
    collection_id = null,
    display = "scalar",
  }: {
    database?: number | null;
    query?: string;
    collection_id?: number | null;
    display?: string;
  } = {},
) {
  const card = {
    collection_id,
    dataset_query: {
      database,
      native: { query, "template-tags": {} },
      type: "native",
    },
    display,
    parameters: [],
    visualization_settings: {},
    type: "question",
  };
  await page.goto(`/question#${adhocQuestionHash(card)}`);
}

