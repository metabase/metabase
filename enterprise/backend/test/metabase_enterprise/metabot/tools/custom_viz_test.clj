          (let [plugin (t2/select-one :model/CustomVizPlugin :identifier identifier)
                before (t2/count :model/CustomVizPlugin :identifier identifier)]
            (mt/with-test-user :rasta
              (let [result       (reuse-custom-viz! (str "custom:" identifier)
