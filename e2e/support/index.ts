const formatArgs = (args: any[]): string => {
  try {
    return args.length > 0
      ? args
          .map(arg =>
            typeof arg === "object"
              ? JSON.stringify(arg, null, 2)
              : String(arg),
          )
          .join(", ")
      : "";
  } catch (e) {
    return "Unable to stringify args";
  }
};

