                (is (= :custom-visualization (:result-type structured)))
                (is (= (:id plugin) (:custom-viz-plugin-id structured)))
                (is (= identifier (:identifier structured)))
                (is (= "Reusable Star Bars" (:display-name structured)))
                (is (= description (:description structured)))
                (is (nil? (:repository-path structured)))
                (is (not (str/includes? (:output result) "<repository-path>")))
                (is (= streaming/adhoc-viz-type (get-in result [:data-parts 0 :data-type])))
                (is (= (str "custom:" identifier) (:display decoded)))
                (is (= "reused stars" (get-in decoded [:visualization_settings :label]))))))
          (finally
            (t2/delete! :model/CustomVizPlugin :identifier identifier)))))))

(deftest create-custom-visualization-tool-rejects-malformed-factory-test
  (premium-features.tu/with-premium-features #{:custom-viz}
    (mt/as-admin
      (let [memory (atom {:state {:queries {"orders-by-month" (query)}}})]
        (binding [shared/*memory-atom* memory]
          (testing "a factory_js with a JavaScript syntax error is rejected and not persisted"
            (let [before (t2/count :model/CustomVizPlugin)
                  result (agent-tools/create-custom-visualization-tool
                          {:query_id "orders-by-month"
                           :display_name "Broken Viz"
                           :description "Broken custom visualization test."
                           :factory_js "function () { return { mount: function (c, p) { var x = ; } }; }"})]
              (is (nil? (:structured-output result))
                  "no structured output is returned for a malformed visualization")
              (is (str/includes? (:output result) "failed validation")
                  "the agent receives a validation error message")
              (is (re-find #"(?i)syntax" (:output result))
                  "the message identifies the JavaScript error")
              (is (= before (t2/count :model/CustomVizPlugin))
                  "no plugin row is created for a malformed visualization")))
          (testing "a factory_js whose mount throws at runtime is rejected"
            (let [result (agent-tools/create-custom-visualization-tool
                          {:query_id "orders-by-month"
                           :display_name "Throwing Viz"
                           :description "Throwing custom visualization test."
                           :factory_js (str "function () { return { id: 'x', getName: function () { return 'X'; },"
                                            " mount: function (container, props) { return props.series[0].data.rows[0].nope.boom; } }; }")})]
              (is (nil? (:structured-output result)))
              (is (str/includes? (:output result) "failed validation")))))))))

