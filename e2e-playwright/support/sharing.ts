export async function openSharingMenu(
  page: Page,
  menuItemText?: string | RegExp,
) {
  await sharingMenuButton(page).click();
  if (menuItemText) {
    await sharingMenu(page)
      .getByText(menuItemText, { exact: typeof menuItemText === "string" })
      .click();
  }
}

// === ports of e2e-embedding-helpers.js ===

/**
 * Port of H.openNewPublicLinkDropdown: opens the sharing menu's public-link
 * item and resolves with the uuid from the POST .../public_link response
 * (the Cypress helper stashed it in the "@uuid" alias).
 */
export async function openNewPublicLinkDropdown(
  page: Page,
  resourceType: "card" | "dashboard",
): Promise<string> {
  const publicLinkResponse = page.waitForResponse(
    (response) =>
      response.request().method() === "POST" &&
      new RegExp(`^/api/${resourceType}/[^/]+/public_link$`).test(
        new URL(response.url()).pathname,
      ),
  );
  await openSharingMenu(page, /public link/i);
  const { uuid } = (await (await publicLinkResponse).json()) as {
    uuid: string;
  };
  return uuid;
}

export async function createPublicQuestionLink(
  api: MetabaseApi,
  questionId: number,
): Promise<{ uuid: string }> {
  const response = await api.post(`/api/card/${questionId}/public_link`, {});
  return (await response.json()) as { uuid: string };
}

