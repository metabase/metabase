"""Message types for LLM communication."""

from typing import Any, Literal

from pydantic import BaseModel, ConfigDict, Field, field_validator


class NoAdditionalPropsBaseModel(BaseModel):
    model_config = ConfigDict(extra="forbid")


class ToolCall(NoAdditionalPropsBaseModel):
    id: str = Field(description="The ID of the tool call.")
    name: str = Field(description="The name of the function.")
    arguments: str = Field(description="The arguments of the function as JSON.")

    def model_dump_open_ai(self) -> dict[str, Any]:
        return {
            "id": self.id,
            "type": "function",
            "function": {
                "name": self.name,
                "arguments": self.arguments,
            },
        }


class CacheControl(BaseModel):
    """Cache control for Anthropic prompt caching."""

    type: Literal["ephemeral"] = "ephemeral"


class BaseMessage(NoAdditionalPropsBaseModel):
    content: str = Field(description="The contents of the message.")

    def model_dump_open_ai(self) -> dict[str, Any]:
        return self.model_dump(include=["role", "content"])

    def to_ai_sdk_chunks(self) -> list[str]:
        return []


class TextMessageContent(BaseModel):
    type: Literal["text"] = "text"
    text: str
    cache_control: CacheControl | None = Field(default=None, description="Cache control for prompt caching.")


class ImageUrl(BaseModel):
    url: str


class ImageMessageContent(BaseModel):
    type: Literal["image_url"] = "image_url"
    image_url: ImageUrl


MultiContent = TextMessageContent | ImageMessageContent


class UserMessage(BaseMessage):
    role: Literal["user"] = Field(
        description="The role of the messages author, in this case `user`.",
        default="user",
    )
    content: str | list[MultiContent] = Field(description="The contents of the message.")

    def model_dump_open_ai(self) -> dict[str, Any]:
        return self.model_dump(include=["role", "content"])


class SystemMessage(BaseMessage):
    role: Literal["system"] = Field(
        description="The role of the messages author, in this case `system`.",
        default="system",
    )


class AssistantMessage(BaseMessage):
    content: str | None = Field(default="", description="The contents of the message.")
    role: Literal["assistant"] = Field(
        description="The role of the message's author, in this case `assistant`.",
        default="assistant",
    )
    tool_calls: list[ToolCall] | None = Field(description="The tool calls generated by the model.", default=None)
    navigate_to: str | None = Field(
        description="Path where the user should be navigated to.",
        default=None,
    )

    def model_dump_open_ai(self) -> dict[str, Any]:
        dump = super().model_dump_open_ai()
        if self.tool_calls:
            dump["tool_calls"] = [tc.model_dump_open_ai() for tc in self.tool_calls]
        return dump

    def to_ai_sdk_chunks(self) -> list[str]:
        from src.llm import streaming

        chunks = []
        if self.content:
            chunks.append(streaming.create_text_part(self.content))
        if self.tool_calls:
            for tool_call in self.tool_calls:
                chunks.append(
                    streaming.create_tool_call_part(
                        tool_call_id=tool_call.id,
                        tool_name=tool_call.name,
                        arguments=tool_call.arguments,
                    )
                )
        if self.navigate_to:
            chunks.append(
                streaming.create_data_part(
                    data_type=streaming.AISDKDataTypes.NAVIGATE_TO,
                    version=1,
                    value=self.navigate_to,
                )
            )
        return chunks


class ToolCallResult(BaseMessage):
    role: Literal["tool"] = Field(
        description="The role of the message's author, in this case `tool`.",
        default="tool",
    )
    tool_call_id: str = Field(description="The ID of the tool call.")

    def model_dump_open_ai(self) -> dict[str, Any]:
        return {
            "content": self.content,
            "role": self.role,
            "tool_call_id": self.tool_call_id,
        }

    def to_ai_sdk_chunks(self) -> list[str]:
        from src.llm import streaming

        return [streaming.create_tool_result_call_part(tool_call_id=self.tool_call_id, result=self.content)]


Messages = UserMessage | AssistantMessage | SystemMessage | ToolCallResult


class FunctionSchemaOpenAi(BaseModel):
    name: str = Field(description="The name of the tool.")
    description: str = Field(description="A description of the tool.")
    parameters: dict[str, Any] = Field(description="JSON schema for the function parameters.")
    strict: bool = Field(default=True, description="Whether to use OpenAI's structured output mode.")


class OpenAIToolSchema(NoAdditionalPropsBaseModel):
    type: Literal["function"] = "function"
    function: FunctionSchemaOpenAi


class ToolSchema(NoAdditionalPropsBaseModel):
    name: str = Field(description="The name of the tool.")
    description: str = Field(description="A description of the tool.")
    parameters: dict[str, Any] = Field(description="JSON schema for the function parameters.")
    examples: list[str] | None = None

    @field_validator("parameters")
    def check_additional_properties_false(cls, v):
        if v and v.get("additionalProperties") is not False:
            raise ValueError("additionalProperties must be set to false.")
        return v

    def model_dump_open_ai(self) -> dict[str, Any]:
        return OpenAIToolSchema(function=FunctionSchemaOpenAi(**self.model_dump(exclude={"examples"}))).model_dump()
