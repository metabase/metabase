/**
 * Shared home for the dashboard-filters-matrix family
 * (e2e/test/scenarios/dashboard-filters-matrix/*.cy.spec.ts).
 *
 * Upstream splits one 216-case parameterised suite across 11 generated
 * `page-N.cy.spec.ts` files, each a two-line wrapper calling `runPage(N)` from
 * the shared `helpers/matrix-helpers.ts` (which slices `helpers/matrix.ts`).
 *
 * This module ports BOTH the runner machinery AND the full 216-entry `matrix`
 * data array — because the data array is the family's shared resource, not a
 * per-page helper. With it here, every page spec (page-0..page-10) is a thin
 * three-line spec that just calls `runPage(N)`, exactly like the Cypress
 * originals. Do NOT re-slice the data per page.
 *
 * Porting notes:
 * - `it(...)` inside `run()` becomes Playwright `test(...)`, generated at
 *   collection time under a `test.describe`. `test`/`expect` come from
 *   ./fixtures.
 * - `H.restore()` / `cy.signInAsAdmin()` → `mb.restore()` / `mb.signInAsAdmin()`.
 * - `cy.request("PUT", "/api/field/:id", ...)` → `mb.api.put(...)`.
 * - `H.createQuestion` / `H.createDashboardWithQuestions` /
 *   `H.updateDashboardCards` / `H.visitDashboard` → the shared factory / helper
 *   ports (factories.ts, dashboard-core.ts, ui.ts).
 * - `H.filterWidget().contains(name).click()` → filterWidget(page).filter(...).
 * - `cy.findByTestId("loading-indicator").should("not.exist")` → toHaveCount(0).
 * - `cy.findByTestId(component).should("be.visible")` → toBeVisible().
 */
import type { Page } from "@playwright/test";

import type { MetabaseApi } from "./api";
